x1 = input('What is your first value?')
x2 = input('What is your second value?')
x1 = int(x1)
x2 = int(x2)
calculate = input('What is your opperation?')
if calculate == '*':
    print(x1, '*', x2, '=', x1*x2)
elif calculate == '/':
    print(x1, '/', x2, '=', x1/x2)
elif calculate == '+':
    print(x1, '+', x2, '=', x1+x2)
elif calculate == '-':
    print(x1, '-', x2, '=', x1-x2)
